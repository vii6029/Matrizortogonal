def igualdade(l1,l2):
    if(len(l1) != len(l2)):
        igual=False
    else:
        for i in range(len(l1)):
            if(l1[i] in l2):
                igual=True
            else:
                igual=False
        return igual 

        

        
       
       

        
            
          
          

 
        
        
    
  
    

    

    






            

